    <p>
        Muflón lesný (Ovis musimon) je jediný voľne žijúci zástupca rodu ovca v európskej prírode. 
        Zároveň je najmenším zástupcom divokých oviec a považuje sa za predchodcu domácich oviec.
    </p>    
    <p>
        Muflón môže byť dlhý 120 (ovce) až 135 (barany), v kohútiku dosahuje výšky  70 (ovce) až 88 cm (barany). 
        Chvost meria 10 cm. Staršie barany dosahujú váhu 50 kg, ovce okolo 35 kg. 
        Muflonia zver má na hlave rohy. 
        Od parožie jeleňovitých sa líšia tým, že sú duté, nevetvené, zver ich nezhadzuje, a aj svojím pôvodom. 
        Sú produktom kože, ako srsť či ratice, a nemajú charakter kosti. 
        Veľké, zahnuté a silno vyvinuté rohy nosia iba barany, ktorí ich používajú pri súbojoch. 
        Bývajú dlhé až 80 cm. 
        Samice sú buďto bezrohé, alebo nosia malé rožky, dlhé najviac 16 cm. 
        Povrch rohov je vrúbkovaný a podľa vrubov možno poznať vek muflónov.
    </p>
    <p>
        Najlepšie sa im darí na skalách a kamenistej pôde, kde si môžu dobre obrusovať neustále dorastajúce kopytá – ratice. 
        U muflónov chovaných na mäkkom podklade ratice prerastajú a spôsobujú krívanie zvierat.
    </p>
    <p>
        Muflón nepatrí medzi vyberavé druhy a žerie kyslé a tvrdé trávy. 
        V celkovom objeme prijímanej potravy tvoria trávy asi 70 %, lístie stromov a kríkov tvorí zvyšok. 
        Ak nenájde dostatok a vhodné zloženie potravy, ohrýza koreňové nábehy lesných drevín, prípadne kmene.
    </p>
    <p>
        Je to typický stádový druh a celý rok žije v rôzne početných čriedach (asi 20 členov) rozdelených podľa pohlavia, 
        iba staršie barany žijú radšej samotársky. 
        Muflóny majú veľmi dobre vyvinutý sluch,čuch i zrak a dokážu zaznamenať človeka na vzdialenosť viac ako jeden kilometer.        
    </p>  
    <p>
        Ruja prebieha v novembri, niekedy aj v decembri. 
        Samce zvádzajú tvrdé boje, pri ktorých o seba narážajú rohmi. 
        Mláďatá sa rodia väčšinou po jednom, niekedy po dvoch. 
        Mláďatá sa rodia nielen všetkým dospelým samiciam, ale dokonca i silným samiciam starým iba jeden rok. 
        Mláďatá prichádzajú na svet už začiatkom marca a samičky počas jari a leta tak fyzicky vyspejú, že koncom roka už prichádzajú do ruje. 
        Tieto samice potom privádzajú na svet mláďatá koncom júna. 
        Hlas muflónov je podobný hlasu oviec, je to krátky bľakot, ktorý už z diaľky označuje tiahnúce či pasúce sa stádo. 
        Varovným signálom pri vzrušení je ostré syčanie.
    </p>
